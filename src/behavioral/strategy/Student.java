package behavioral.strategy;

public class Student {

    private String name;
    private int idnp;

    public Student(String name, int idnp) {
        this.name = name;
        this.idnp = idnp;
    }

    public String getName() {
        return name;
    }

    public int getIdnp() {
        return idnp;
    }

}
